    """
    Esse teste tem 3 etapas (AAA)
    - A: Arrange - Arranjo
    - A: Act     - Executa a coisa (SUT)
    - A: Assert  - Garanta que A é A
    """
    # Arrange
    # client = TestClient(app)

    # Act
    response = client.get('/')

